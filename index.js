function add(a, b){
        return a + b;
}

function subtract(c, d){
        return c - d;
}

function multiply(e, f){
        return e * f;
}

function divide(g, h){
        return g / h;
}

function increment(n) {
        return (n + 1);
}

function decrement(m) {
        return (m - 1)
}

function makeInt(a) {
        return parseInt(a, 10)
}

function preserveDecimal(a) {
        return parseFloat(a, 10);
}